    function splitData(rawData) {
        var categoryData = [];

        var values = []
        for (var i = 0; i < rawData.length; i++) {
            categoryData.push(rawData[i].splice(0, 1)[0]);

            values.push(rawData[i])
        }
        return {
            categoryData: categoryData,
            values: values
        };
    }






